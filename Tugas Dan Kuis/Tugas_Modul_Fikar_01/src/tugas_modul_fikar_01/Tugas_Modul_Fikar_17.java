package tugas_modul_fikar_01;
public class Tugas_Modul_Fikar_17 
{
    public static void main(String[] args) 
    {
        int ut = 18;
        do 
        {
            int ck = ut; 
            do 
            {
                System.out.print(ck + " ");
                ck = ck + 1;
            } 
            while (ck <= 23); 
            System.out.println("");
            ut = ut + 1;
        } 
        while (ut <= 23);
        System.out.println("");
        System.out.println("FIKAR DWI RAMADHANI 0022");
    }
}
